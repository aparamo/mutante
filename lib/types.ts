import { z } from "zod";

// References (links to videos, pdfs, articles)
export const ReferenceSchema = z.object({
  _id: z.string(), // MongoDB ObjectId as string - now REQUIRED
  title: z.string(),
  url: z.string().url().or(z.literal("")).nullish(),
  type: z.enum(["video", "article", "book", "social", "website", "other", "thesis", "interview"]).optional().default("other"),
  description: z.string().nullish(), // Brief summary, findings, or relevance
  year: z.number().int().nullish(), // Year of publication
  magazine: z.string().nullish(), // Name of the magazine/journal if it's an article
  fullCitation: z.string().nullish(), // Full, formatted citation (e.g., APA, Harvard)
  keywords: z.array(z.string()).optional().default([]), // Keywords related to the work
  isbn: z.string().nullish(), // ISBN if it's a book
  isFundamental: z.boolean().optional().default(false), // Is it one of their most important/recognized works?
  isValidated: z.boolean().optional().default(false), // Admin validation flag
  isAiGenerated: z.boolean().optional().default(false), // AI generated flag
  pdfUrl: z.string().url().or(z.literal("")).nullish(), // UploadThing URL or external verified PDF
  markdownPath: z.string().nullish(), // Local path to /public/know/.../content.md
  textContent: z.string().nullish(), // Full text content of the PDF for RAG
});

export type Reference = z.infer<typeof ReferenceSchema>;

// Detailed Social & Links
export const LinksSchema = z.object({
  linkedin: z.string().url().optional().or(z.literal("")),
  twitter: z.string().url().optional().or(z.literal("")),
  website: z.string().url().optional().or(z.literal("")),
  organizationName: z.string().optional(),
  organizationUrl: z.string().url().optional().or(z.literal("")),
});

export type Links = z.infer<typeof LinksSchema>;

// The main Expert schema
export const ExpertSchema = z.object({
  _id: z.string().optional(), // MongoDB ObjectId
  id: z.string().optional(), // Can be the MongoDB ObjectId as string
  name: z.string().min(1, "Name is required"),
  cohort: z.number().int().positive(),
  sector: z.string().optional(),
  title: z.string().optional(), // The historical title from the PDFs
  currentTitle: z.string().optional(), // The updated/current job title found by AI
  email: z.string().email().optional().or(z.literal("")).catch(""),

  // -- Phase 2 Fields (Enriched) --
  isEnriched: z.boolean().optional().default(false), // Flag to track progress
  isValidated: z.boolean().optional().default(false), // Profile validation by admin
  isAiGenerated: z.boolean().optional().default(false), // Profile data generated by AI flag
  bio: z.string().optional(), // AI generated biography
  links: LinksSchema.optional(), // Social and Web links
  references: z.array(ReferenceSchema).optional().default([]), // Found links
  topics: z.array(z.string()).optional().default([]), // Key topics (e.g., "Desarrollo Rural", "Género")

  // -- Vector for AI --
  embedding: z.array(z.number()).optional(), // Mathematical vector for RAG
});

export type Expert = z.infer<typeof ExpertSchema>;

export interface EnrichedExpertData {
  bio: string;
  currentTitle: string;
  topics: string[];
  links?: {
    linkedin?: string | null;
    twitter?: string | null;
    website?: string | null;
    organizationName?: string | null;
    organizationUrl?: string | null;
  };
  references?: {
    title: string;
    type?: string;
    description?: string | null;
    year?: number | null;
    keywords?: string[];
    isbn?: string | null;
    isFundamental?: boolean;
  }[];
}

// -- Citations Schema --
// To store fundamental data, experiences, and deep lessons
export const CitationSchema = z.object({
  _id: z.string().optional(), // MongoDB ObjectId
  id: z.string().optional(), // MongoDB ObjectId
  expertId: z.string().optional(), // Link to the Expert
  expertName: z.string(), // Denormalized for easier querying
  quote: z.string(), // The exact quote or synthesized fundamental lesson
  sourceTitle: z.string().optional(), // Title of the book, article, video
  sourceUrl: z.string().url().optional().or(z.literal("")), // Link to the source
  date: z.string().optional(), // Approximate date of the quote/lesson
  context: z.string().optional(), // Brief context of the quote
  topics: z.array(z.string()).optional().default([]), // Topics related to the quote
  
  // -- Validation Flags --
  isValidated: z.boolean().optional().default(false), // Admin validation flag
  isAiGenerated: z.boolean().optional().default(false), // Extracted by AI flag
  
  // -- Vector for AI --
  embedding: z.array(z.number()).optional(), // Semantic search vector
});

export type Citation = z.infer<typeof CitationSchema>;
